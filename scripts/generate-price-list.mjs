// Generate a fresh price list of all Steven Angel products for the Marketing agent.
//
// Sources:
//   - Ghost catalog: live backend (/ghost/tracks → SQLite source of truth)
//   - Shop templates + masterclass: src/shop/products.js
//   - Mix Mastering packages: ghost-backend index.js (hardcoded mirror below)
//   - Custom Ghost services: src/GhostCustom.jsx PRICING_ROWS (hardcoded mirror)
//   - Lessons: src/Lessons.jsx (hardcoded mirror)
//
// Output: claude marketing/inputs/price_list.md
//   Marketing agent reads this file. Re-run any time a product changes:
//     npm run prices
//
// Run from steven-angel-website-final/:
//   node scripts/generate-price-list.mjs
//
// Or auto-run on every build via the package.json `prebuild` hook.

import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { PRODUCTS } from "../src/shop/products.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const OUT_FILE = "/Volumes/Untitled/Dropbox/Busniees assets 2026/claude marketing/inputs/price_list.md";

const BACKEND = "https://ghost-backend-production-adb6.up.railway.app";

// ── Hardcoded mirrors of code that lives in JSX files ────────────────────────

const MIX_MASTERING = [
  { id: "mastering",        name: "Mastering",                              priceUsd: 35,  type: "Mix & Mastering" },
  { id: "stem-mastering",   name: "Stem Mastering",                         priceUsd: 75,  type: "Mix & Mastering" },
  { id: "mix-master",       name: "Mix + Master",                           priceUsd: 150, type: "Mix & Mastering" },
  { id: "mix-master-large", name: "Mix + Master (30-100 stems)",            priceUsd: 250, type: "Mix & Mastering" },
];

const GHOST_SERVICES = [
  { id: "ghost-demo-finishing", name: "Demo Finishing",                priceUsd: 300,  type: "Ghost Service" },
  { id: "ghost-full-production", name: "Full Production",              priceUsd: 800,  type: "Ghost Service" },
  { id: "ghost-full-vocals",    name: "Full Production + Vocals",     priceUsd: 1500, priceLabel: "1,500+", type: "Ghost Service" },
];

const LESSONS = [
  { id: "lesson-intro",  name: "Instruction Lesson (intro)", priceUsd: 30,  detail: "30 min",         type: "Lessons" },
  { id: "lesson-1hr",    name: "Single Lesson",              priceUsd: 120, detail: "1 hour",         type: "Lessons" },
  { id: "lesson-3pack",  name: "3 Lessons",                  priceUsd: 320, detail: "Best entry",     type: "Lessons" },
  { id: "lesson-6pack",  name: "6 Lessons",                  priceUsd: 580, detail: "Full module",    type: "Lessons" },
];

// ── Build the table ──────────────────────────────────────────────────────────

const fmtUsd = (n, label) => label ? `$${label}` : `$${n}`;
const fmtEur = (n) => `€${n}`;

async function main() {
  // 1. Fetch live Ghost catalog
  let ghost = [];
  try {
    const r = await fetch(`${BACKEND}/ghost/tracks`);
    const data = await r.json();
    ghost = (data.tracks || [])
      .filter((t) => !String(t.id || "").startsWith("test-"))
      .sort((a, b) => (b.price_eur || 0) - (a.price_eur || 0));
  } catch (err) {
    console.error("[prices] Could not fetch Ghost catalog:", err.message);
  }

  // 2. Filter shop products (drop test-)
  const shop = (PRODUCTS || []).filter((p) => !String(p.id).startsWith("test-"));

  const date = new Date().toISOString().slice(0, 10);
  const lines = [];
  lines.push(`# Steven Angel — Master Price List`);
  lines.push(``);
  lines.push(`> Auto-generated by \`scripts/generate-price-list.mjs\` on ${date}.`);
  lines.push(`> Re-run after adding any new product: \`npm run prices\` from \`steven-angel-website-final/\`.`);
  lines.push(``);
  lines.push(`---`);
  lines.push(``);

  // ── SECTION 1: Ghost catalog (live from backend)
  lines.push(`## 🎛️ Ghost Catalog — Ready-Made Tracks (${ghost.length})`);
  lines.push(``);
  lines.push(`Pre-made productions for sale, one-time exclusive sale per track.`);
  lines.push(``);
  lines.push(`| Track | Price | Genre | Status |`);
  lines.push(`|---|---:|---|---|`);
  for (const t of ghost) {
    const status = t.sold ? "🔴 SOLD" : "🟢 Available";
    lines.push(`| ${t.name} | ${fmtEur(t.price_eur)} | ${t.genre} | ${status} |`);
  }
  lines.push(``);

  // ── SECTION 2: Ghost Services (custom production)
  lines.push(`## 👻 Ghost Services — Custom Production`);
  lines.push(``);
  lines.push(`Custom-built tracks made to order. Pages: \`/ghost/custom\`, \`/ghost/finish-demo\`.`);
  lines.push(``);
  lines.push(`| Service | Price | Page |`);
  lines.push(`|---|---:|---|`);
  for (const s of GHOST_SERVICES) {
    const page = s.id === "ghost-demo-finishing" ? "/ghost/finish-demo" : "/ghost/custom";
    lines.push(`| ${s.name} | ${fmtUsd(s.priceUsd, s.priceLabel)} | ${page} |`);
  }
  lines.push(``);

  // ── SECTION 3: Mix & Mastering
  lines.push(`## 🎚️ Mix & Mastering — Service Packages`);
  lines.push(``);
  lines.push(`Page: \`/mix-mastering\`. Backend dictionary in \`ghost-backend/index.js MIX_MASTERING_PACKAGES\`.`);
  lines.push(``);
  lines.push(`| Package | Price |`);
  lines.push(`|---|---:|`);
  for (const m of MIX_MASTERING) {
    lines.push(`| ${m.name} | ${fmtUsd(m.priceUsd)} |`);
  }
  lines.push(``);

  // ── SECTION 4: Lessons
  lines.push(`## 🎓 Lessons — Production / Mix / Mastering`);
  lines.push(``);
  lines.push(`Page: \`/lessons\`. Booked via Calendly per package.`);
  lines.push(``);
  lines.push(`| Package | Price | Detail |`);
  lines.push(`|---|---:|---|`);
  for (const l of LESSONS) {
    lines.push(`| ${l.name} | ${fmtUsd(l.priceUsd)} | ${l.detail} |`);
  }
  lines.push(``);

  // ── SECTION 5: Shop (templates + masterclass)
  lines.push(`## 🛍️ Shop — Templates & Masterclass (${shop.length})`);
  lines.push(``);
  lines.push(`Page: \`/shop\`. Products in \`src/shop/products.js\`. PayPal Smart Buttons checkout.`);
  lines.push(``);
  lines.push(`| Product | Price | Genre | Type |`);
  lines.push(`|---|---:|---|---|`);
  for (const p of shop) {
    const type = p.type === "course" ? "Masterclass" : "Ableton Template";
    lines.push(`| ${p.name} | ${fmtUsd(p.price)} | ${p.genre || "—"} | ${type} |`);
  }
  lines.push(``);

  // ── Footer / metadata
  lines.push(`---`);
  lines.push(``);
  lines.push(`## Notes for Marketing`);
  lines.push(``);
  lines.push(`- **Currencies:** Ghost catalog in EUR (€). Everything else in USD ($).`);
  lines.push(`- **Coupons live:** \`WELCOME15\` (15% shop) · \`WELCOME50\` (50% shop) · \`GHOST25\` (25% ghost) · \`MIXMASTER50\` (50% mix-master)`);
  lines.push(`- **Source of truth:**`);
  lines.push(`  - Ghost catalog → live backend SQLite, via \`GET /ghost/tracks\``);
  lines.push(`  - Shop → \`steven-angel-website-final/src/shop/products.js\``);
  lines.push(`  - Mix Mastering → \`ghost-backend/index.js\` MIX_MASTERING_PACKAGES`);
  lines.push(`  - Ghost Services → \`steven-angel-website-final/src/GhostCustom.jsx\` PRICING_ROWS`);
  lines.push(`  - Lessons → \`steven-angel-website-final/src/Lessons.jsx\` packages array`);
  lines.push(`- **Auto-update:** every \`npm run build\` regenerates this file (via \`prebuild\` hook).`);
  lines.push(`- **Manual update:** \`cd steven-angel-website-final && npm run prices\``);
  lines.push(``);

  const md = lines.join("\n");
  writeFileSync(OUT_FILE, md, "utf8");
  console.log(`✓ Wrote ${md.length} bytes to ${OUT_FILE}`);
  console.log(`  Ghost: ${ghost.length} · Shop: ${shop.length} · Mix: ${MIX_MASTERING.length} · Ghost services: ${GHOST_SERVICES.length} · Lessons: ${LESSONS.length}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
